def get_bolt_dimensions(diameter: int, length: int) -> Optional[Dict[str, Any]]:
    """
    Получить размеры болта по диаметру и длине.

    Args:
        diameter: Диаметр болта (мм)
        length: Длина болта (мм)

    Returns:
        Dict с размерами или None если не найдено
    """
    key = f"{diameter}_{length}"
    if key not in BOLT_DIM_DATA:
        return None

    data = BOLT_DIM_DATA[key]
    return {
        "length": data[0],
        "hook_length": data[1],
        "bend_radius": data[2],
        "diameter": data[3],
        "thread_length": data[4],
        "mass_1_1": data[5],
        "mass_1_2": data[6],
        "mass_2_1": data[7],
        "mass_5": data[8],
        "l1": data[9],
        "l2": data[10],
        "l3": data[11],
        "r": data[12],
    }
