def get_bolt_dimensions(diameter: int, length: int) -> Optional[Dict[str, Any]]:
    """
    Получить размеры болта по диаметру и длине.

    Args:
        diameter: Диаметр болта (мм)
        length: Длина болта (мм)

    Returns:
        Словарь с размерами или None, если болт не найден
    """
    key = f"{diameter}_{length}"
    if key not in BOLT_DIM_DATA:
        return None

    data = BOLT_DIM_DATA[key]
    return {
        "L": data[0],  # Длина
        "l": data[1],  # Вылет крюка
        "R": data[2],  # Радиус загиба
        "d": data[3],  # Диаметр
        "l0": data[4],  # Длина резьбы
        "mass_1.1": data[5],
        "mass_1.2": data[6],
        "mass_2.1": data[7],
        "mass_5": data[8],
        "l1": data[9],
        "l2": data[10],
        "l3": data[11],
        "r": data[12],
    }
